from __future__ import annotations

from pathlib import Path


def resolve_project_root() -> Path:
    """Возвращает абсолютный путь к корню текущего проекта.

    Корень вычисляется относительно файла модуля, а не текущей рабочей
    директории процесса. Это делает код устойчивым к запуску из разных
    каталогов и из notebook, батников или модулей `python -m`.
    """
    return Path(__file__).resolve().parents[1]


def ensure_directory(path: Path) -> Path:
    """Гарантирует существование каталога и возвращает его путь.

    Функция создаёт всю недостающую иерархию директорий и безопасно
    отрабатывает при повторных вызовах. Используется перед экспортом
    промежуточных, финальных и QA-артефактов пайплайна.
    """
    path.mkdir(parents=True, exist_ok=True)
    return path
